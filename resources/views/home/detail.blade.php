        <div class="single-text"> 
         <p>当金正恩上台的时候，外面的世界对这个不到30岁的年轻人还有些期待，希望有西方留学经历的他能为这个破败不堪的国家带去一些朝气，然而他除了把自己吃得越来越胖并娶了个貌美如花的乐团主唱之外，还大肆清洗对自己不敬的前朝元老，甚至传出了当众“炮决”“犬决”“机关枪扫射”等骇人听闻的杀人手段。</p> 
         <p>更让外界感到忧虑的是，他带领下的朝鲜在“宇宙第一强国”的道路上越走越远，金正恩上任5年来射出去的导弹超过了此前几十年的总和，从今年年初到现在已经进行了两次核爆。</p> 
         <p>如今的金正恩已经脱去了上台之初的一脸稚气，他指挥发射导弹核试验、视察军队、工地、渔场甚至养猪厂时开心大笑的照片频频登上国际媒体的版面，这是他在对外展现人民拥戴和政权稳固的自信。</p> 
         <p><a href="images/jinzhengen.jpg" class="prettyPhoto_gall" rel="prettyPhoto[gallery1]"><img class="aligncenter size-full wp-image-5137" src="images/jinzhengen.jpg" alt="金正恩" title="这么多大国，为什么治不了一个朝鲜？" width="547" height="381" /></a></p> 
         <p>如果说前几次朝鲜核试验外界还是怀疑他的核技术，那么第五次核爆清楚地告诉世人，如果继续这样下去，朝鲜拿出可以打到美国本土的战略核导弹只是个时间问题，打到中日韩就更不用说了。</p> 
         <p>朝鲜在研制核武器的道路上，只用了一个策略：明修栈道，暗度陈仓，嘴上反复说放弃，甚至还炸过冷却塔、允许国际组织进去核查，但实际上从来也没有住手；在六方会谈中也出尔反尔，签了停核协议，没几年就撕毁，宣布自己已是拥核国家，还要求大家都承认。在国际社会上，基本没有什么信誉可言。</p> 
         <p>连肚子都填不饱，为什么还要一意孤行搞这些“大杀器”？朝鲜的行为常常令外界感到困惑。</p> 
         <p>要知道朝鲜人脑子里装的什么，就要了解朝鲜这台国家机器的灵魂——主体思想，它控制了从国家领袖、党国精英直至大头百姓的一切行动。朝鲜的说法是，领袖是大脑，党和军队、政府是手足，人民是躯干，领袖指哪儿就打哪儿。</p> 
         <p>“主体思想”是朝鲜开国领袖金日成创立的，地位相当于中国的“毛泽东思想”（水平高低另当别论），后被写入朝鲜宪法，成为国家和人民唯一的活动指导理论，至高无上。</p> 
         <p>主体思想被确立为朝鲜的立国之本，核心可以概括为四个字：独立、自主。“主体”的意思就是以朝鲜自己为主体，政治上自主、经济上自足、国防上自卫。简单说，就是谁都不能信，只能靠自己。</p> 
         <p>在外界看来是不守承诺，但在朝鲜看来，这就是主体思想的体现，谁的面子也不给，我自己决定自己的命运，爱怎么来怎么来，谁也管不着。</p> 
         <p>朝鲜从战后直到80年代末取得的经济建设成就，为主体思想提供了合法性，然而到了90年代，因为苏联解体和东欧剧变，外来援助断绝，朝鲜国内经济急剧恶化，发生持续数年的饥荒，为了稳定政权，朝鲜领导人金正日在主体思想的基础上提出“先军政治”：一切工作都围绕军队展开，有限的资源要优先保障军队。只要控制好军队，对外保持武力威慑，对内确保政权根基稳固，因此即便发生大饥荒，朝鲜国内也没有发生动乱。</p> 
         <p>朝鲜军队目前保持在110万人左右，数量仅次于中美印排第四位，军民比例世界第一。军队在朝鲜政治生活当中地位很高，生活待遇、政治待遇、社会声誉比工人、知识分子都好，年轻人一般都喜欢当兵，年轻的姑娘想找对象，也喜欢找军人。</p> 
         <p>先军政治的逻辑就是，要先实现的独立自主地生存下去，然后再讨论生活的问题，小孩从小就被教育“没有糖果可以活下去，没有子弹就不能生存”。</p> 
         <p>朝鲜最高领导人也多次公开表示“先军政治就是万能宝剑”，强化先军政治也是金氏政权维持合法性的唯一手段，一旦搞出核武器，对内可以证明先军思想和领袖的伟大英明，振奋民心，给人灌输“我日子过得没你好，但我比你厉害”的精神鸦片。对外，则可以此作为筹码，向国际社会要粮食要原油，反正光脚的不怕穿鞋的，大不了同归于尽。</p> 
         <p>在朝鲜，主体思想和先军政治无处不在，比如朝鲜不使用公元纪年，而是使用主体纪年，以金日成诞生的1912年为主体元年，今年是主体105年；平壤地标叫“主体思想塔”，在日常生活中，主体思想标语随处可见。</p> 
         <p>由于没有签订终战和平条约，理论上，朝鲜半岛是世界上不多的仍然处于战争状态的地方，官方宣传中，将美国、韩国、日本树立成无时不在的敌人，现在偶尔也指桑骂槐地捎上中国。这种几十年如一日洗脑式的宣传是成功的，普通民众对领袖的爱戴与宗教信仰几乎没有分别：</p> 
         <p>与主体思想相对的是，朝鲜从金日成至今，都坚决抵制“事大主义”，“事大”就小国侍奉大国以保存自身的策略。古代的朝鲜王朝从中国明初到清末，一直奉行“事大交邻”的政策，“事大”就是侍奉中原王朝，“交邻”则是指与日本等邻国的往来。因此“北不失礼，南不失信”成为朝鲜王朝的祖训，而“事大”则成为朝鲜对华政策的代名词。</p> 
         <p>“事大”显然与主体思想是矛盾的，不论是朝鲜上世纪50年代对曾在中国延安、太行山从事抗日斗争的朝鲜共产党人进行清洗，还是官方宣传和教科书中向来极少提及朝鲜战争时的中国志愿军，都是在“树立主体，反对事大”，消除民间的中国崇拜，不断强调朝鲜民族的主体性，宣扬民族主义，这对于封闭的、强敌环饲的朝鲜来说，很容易培养起强大的群众基础。</p> 
         <p>中朝关系历来也不是由双边关系来界定的。古代两国关系受制于日本，明朝万历年间的壬辰倭乱、清末的甲午战争，都因日本入侵朝鲜，中国出于保护藩属国的道义，出兵抗日，结果直接加速了两个王朝的灭亡；朝鲜战争中国出兵，是为对抗美国。冷战时期，中朝因为意识形态纽带，形成一种同志加兄弟的友谊，但当这种关系赖以为继的外部环境发生变化之后，就会变质。</p> 
         <p>从70年代中美、中日关系正常化以及中国的改革开放开始，中国就从革命外交的思维中走出来，开始了以国家利益为核心的外交时代，融入到美国主导的国际经济体系之中去，这也是中国在40年来取得非凡成就的根本所在。而朝鲜则仍然致力于构建“主体思想”，反对“事大主义”，基于意识形态为纽带的亲密关系逐渐松弛了，现实利益让两国处于两个平行线上。1992年中韩建交，更引发了朝鲜对中国不满。</p> 
         <p>朝鲜深知自己对中国的战略意义，也只有在需要帮助的时候才会想起中国。钱其琛在他的《外交十记》当中，回忆了他亲自去向金日成报告中韩建交的事情，当时钱其琛一行的专机到达平壤之后，机场没有按照惯例举行欢迎仪式，钱其琛一行改乘朝鲜准备的直升机，飞到金日成夏季常住的湖滨别墅。钱其琛向金日成转达了江泽民的口信。</p> 
         <p>金日成当时说，中国的事情，中国定了就可以了，你们就按你们定的做，我们自己走自己的路。需要的时候，我们再请你们帮助吧，就这样吧。在钱其琛的记忆当中，这次会见是金日成历次会见中国代表团时间最短的一次。会见之后，没有举行任何例行的招待宴会，钱其琛一行当天就返回了北京。</p> 
         <p>如今，包括美日韩在内，很多人将朝鲜日益壮大的核力量归罪于中国，认为中国没有对其施加影响力，这其实是没有认清中朝之间真实的关系。中方已经多次声明，中国和朝鲜是正常的国家关系，正常关系就是说，我跟他不存在军事同盟，也不会干涉他的内政。而且，历次核试后联合国对朝鲜的制裁决议，中国都是了投赞成票的，难道这还不够明显吗？</p> 
         <p><strong>还有一些人说，当年中国试验原子弹，美国苏联也坚决反对，如今朝鲜是一个主权国家，为啥你能有原子弹，人家就不能有？这个逻辑忽视了一个国际道义的问题，核武器是一种终极武器，跟炮弹不一样，需要极强的控制能力，还得有大国担当，否则对全人类都是毁灭性的。曾有外国专家去朝鲜考察核设施，对他们的防护设施感到很担忧，虽然他坚决反对朝鲜搞核武器，但还是从人道主义出发，为他们提供了改进设备的技术手段。</strong></p> 
         <p>这就朝鲜核设施的现实情况，这些核设施距离中国边境不过百十公里，一旦发生泄漏，大片东北国土势必遭殃，全世界没有哪个国家在人口这么稠密的地方进行核试验。加上朝鲜的体制和领导人的行事风格，谁能保障他不乱来？万一玩火自焚，大量朝鲜人变成难民涌入中国怎么办？</p> 
         <p>如今中国的地位很尴尬，打，不可能，一言不合就动手的时代已经过去了，中国也绝不会允许有人在自己家门口放火，朝鲜又把2000多万人民和核武器一起绑在战车上，到时候跟你来个鱼死网破，大家就都吃不了兜着了；和，也很难，六方会谈基本破产，重新启动遥遥无期，美国要求朝鲜首先放弃核武器再谈判，朝鲜则要求美国先承认它的核地位再说，互相极度不信任，根本坐不下来。</p> 
         <p>有人提出由中国提供核保护伞来换取朝鲜弃核，且不说这与中国一贯的不结盟政策相违背，朝鲜压根就没打算靠别人的保护伞，否则金氏的合法性在哪里？靠着核保护伞能统一国家吗？</p> 
         <p>因为主体思想，朝鲜不会允许任何大国插手本国事务，对自己施加影响，大国也很难像对叙利亚、伊拉克、利比亚那样，在内部扶植自己的力量。</p> 
         <p>朝鲜领袖就是在这样的地缘格局中生存了下来，比萨达姆、卡扎菲甚至巴沙尔活得都好。</p> 
         <p>如果把国际社会比作丛林，朝鲜就是眼镜蛇，它全身柔软，但有两颗令人胆颤毒牙，从民族国家进化的角度来看，朝鲜是成功的，不管是试射导弹还是核试验，他都是在把这两颗毒牙磨得更加锋利而已，这比上面那些穆斯林国家坐以待毙要高明。</p> 
         <p>（来源：微信公众号“财经也疯狂”）</p>  
